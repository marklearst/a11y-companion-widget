#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const CHECKLIST_PATH = path.resolve(__dirname, "../widget-src/data/checklist.json");
const OUTPUT_PATH = path.resolve(__dirname, "../widget-src/data/wcagLevelMap.ts");

const LEVELS = ["A", "AA", "AAA"];

function normalizeLevel(value) {
  if (!value) return null;
  const normalized = String(value).trim().toUpperCase();
  return LEVELS.includes(normalized) ? normalized : null;
}

function parseCode(value) {
  const match = String(value ?? "").match(/(\d+\.\d+\.\d+)/);
  return match ? match[1] : null;
}

function parseSectionEntries(sectionData) {
  const tasks = Array.isArray(sectionData?.tasks) ? sectionData.tasks : [];
  return tasks
    .map((task) => {
      const level = normalizeLevel(task?.level);
      const code = parseCode(task?.wcag);
      if (!level || !code) return null;
      return {
        level,
        code,
        title: String(task?.title ?? "").trim(),
        url: String(task?.url ?? "").trim(),
      };
    })
    .filter(Boolean);
}

function compareWcagCodes(a, b) {
  const aParts = a.split(".").map(Number);
  const bParts = b.split(".").map(Number);
  for (let index = 0; index < 3; index += 1) {
    const delta = (aParts[index] ?? 0) - (bParts[index] ?? 0);
    if (delta !== 0) return delta;
  }
  return 0;
}

async function main() {
  const raw = await fs.readFile(CHECKLIST_PATH, "utf8");
  const data = JSON.parse(raw);

  const map = {
    A: new Map(),
    AA: new Map(),
    AAA: new Map(),
  };

  for (const sectionData of Object.values(data)) {
    const entries = parseSectionEntries(sectionData);
    for (const entry of entries) {
      const existing = map[entry.level].get(entry.code);
      if (!existing) {
        map[entry.level].set(entry.code, {
          code: entry.code,
          title: entry.title,
          url: entry.url,
        });
      }
    }
  }

  const normalized = {};
  for (const level of LEVELS) {
    const values = [...map[level].values()].sort((left, right) =>
      compareWcagCodes(left.code, right.code),
    );
    normalized[level] = values;
  }

  const criteriaByLevel = {};
  for (const level of LEVELS) {
    criteriaByLevel[level] = normalized[level].map((entry) => entry.code);
  }

  const banner = [
    "/**",
    " * AUTO-GENERATED FILE - DO NOT EDIT DIRECTLY.",
    " *",
    " * Generated by scripts/generate-wcag-level-map.mjs",
    " * Source: widget-src/data/checklist.json",
    " */",
    "",
  ].join("\n");

  const content = [
    banner,
    'import type { WCAGLevel, WcagCriterionCode } from "types/wcag";',
    "",
    "export interface WcagLevelCriterion {",
    "  code: WcagCriterionCode;",
    "  title: string;",
    "  url: string;",
    "}",
    "",
    "export type WcagLevelCriteriaMap = Record<WCAGLevel, readonly WcagLevelCriterion[]>;",
    "export type WcagCriteriaByLevelMap = Record<WCAGLevel, readonly WcagCriterionCode[]>;",
    "",
    `export const wcagLevelCriteriaMap: WcagLevelCriteriaMap = ${JSON.stringify(normalized, null, 2)} as const;`,
    "",
    `export const wcagCriteriaByLevel: WcagCriteriaByLevelMap = ${JSON.stringify(criteriaByLevel, null, 2)} as const;`,
    "",
  ].join("\n");

  await fs.writeFile(OUTPUT_PATH, content, "utf8");
  console.log(`Wrote ${OUTPUT_PATH}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
